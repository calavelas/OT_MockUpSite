function getConsent(){
    fetch('consents.json')
    .then((res) => res.json())
    .then((data) => {
      output = [];
      data.forEach(function(item){
        output += `
          <p>${item.id}. ${item.description}<br><span class="text-success">(OT Consent : ${item.purposeName})</span></p>
          <div class="form-check form-check-inline">
            <input class="form-check-input" type="radio" name="customRadioInline${item.No}" id="${item.id}" required>
            <label class="form-check-label" for="inlineRadio1">Consent</label>
          </div>
          <div class="form-check form-check-inline">
            <input class="form-check-input" type="radio" name="customRadioInline${item.No}" id="${item.id} decline" required>
            <label class="form-check-label" for="inlineRadio2">Decline</label>
          </div>
          <div class="my-3"></div>
          `;
      });
      document.getElementById('autoGeneratedPurpose').innerHTML = output;
    })
  }
  getConsent()